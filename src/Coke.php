<?php
declare(strict_types=1);

namespace App;


class Coke
{
    /**
     * @var string
     */
    private $name;


    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}